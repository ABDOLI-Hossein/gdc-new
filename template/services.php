            <p class="font-size-15 pt-4">
                Most people are recommended to get their teeth and gums checked and cleaned twice a year. However,
                circumstances such as orthodontic treatment, history of adult gum disease, poor oral hygiene, and some cases
                uncontrolled diabetes can require many to get 3-4 times a year dental cleanings.
            </p>
            <p>Gingivitis and Adult Periodontitis are the two major types of gum disease that can be treated simply by
                dental cleaning. There are mainly two types of dental cleanings: regular and deep cleaning. </p>
            <p>Regular cleaning is indicated in case of gingivitis or adult periodontitis that has been previously treated
                and is completely under control with no signs of active infection.<br>
                Deep cleaning is indicated in case of active infection due to adult periodontitis that has either never been
                treated or was treated at some point without patient following up with regular maintenance visits after the
                initial deep cleaning.
            </p>
            <p>
                <span style="text-decoration-line: underline">Gingivitis:</span><br>
                In case of gingivitis, the infection is confined to the gums and there is no adverse effect on the bone
                surrounding the teeth. This type of infection is easily treatable and reversible by getting back into a
                regular maintenance routine and being more discipline with your flossing and brushing.

            </p>
            <p>
                <span style="text-decoration-line: underline">Adult Periodontitis:</span><br>
                In case of Adult Periodontitis, there is not only infection in the gums, but also in the bone surrounding
                the teeth. There is irreversible bone loss that occurs due to this infection. The ultimate goal in treating
                Adult Periodontitis is to stop the infection and the process of bone loss that occurs due to the infection.
                Unfortunately, in most cases that affect the entire mouth, there is no way we could ever reverse the damage
                that has already occurred. The best we can hope for is to stop further bone loss. This is done by initially
                doing deep cleaning, scaling and root planning.

            </p>
            <p>Once a patient has gone through the initial phase of treatment, it is the patient’s homecare and their
                recommended maintenance visits which will determine the success of their
                treatment.
            </p>
            <p>
                Within a month after the deep cleaning, there will be an evaluation visit during which the
                patient’s level of home care is evaluated.
            </p>
            <p>
                Oral hygiene instructions will be reemphasized if necessary and depending on the patient’s following visits,
                they may or may not need the secondary phase of treatment which includes application of localized
                antibiotics or in some cases referral to the gum specialist for further surgical treatment.
            </p>
            <p>
                Patients that have ever had adult periodontitis usually are required to receive 3-4 maintenance cleanings in
                a year to prevent the relapse of the infection.
            </p>
